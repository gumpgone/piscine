int	ft_iterative_factorial(int nb)
{
	int	c;

	c = nb;
	if (nb == 0)
		return (1);
	if (nb < 0)
		return (0);
	while (c > 1)
	{	
		c--;
		nb *= c;
	}
	return (nb);
}
