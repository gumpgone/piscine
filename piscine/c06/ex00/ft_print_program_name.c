#include <unistd.h>

int	main(int argc, char **argv)
{	
	int		i;
	char	c;

	argc = 0;
	i = 0;
	while (argv[0][i])
	{
		c = argv[0][i];
		write(1, &c, 1);
		i++;
	}
	write(1, "\n", 1);
}
